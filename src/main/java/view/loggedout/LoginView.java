        System.out.println(cliente.getCpf());
        
        clientedao.requestCliente(cliente.getCpf());
        
        if (criouCliente) {
            ContaDAO contadao = new ContaDAO();        
            Conta conta = new Conta();

            Banco b = (Banco)jComboBoxBanco1.getSelectedItem();

            conta.setBancoId(b.getId());
            conta.setSenha(jPasswordFieldCriarSenha1.getPassword().toString());
            conta.setClienteId(cliente.getId());
            conta.setNumero(Integer.parseInt(jTextFieldCriarNmr1.getText()));
        }
